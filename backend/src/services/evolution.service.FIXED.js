    /**
     * üóëÔ∏è DELETAR INST√ÇNCIA
     */
    /**
     * üóëÔ∏è DELETAR INST√ÇNCIA (ROBUSTO)
     * Aceita tanto o nome da inst√¢ncia quanto um ID e tenta resolver para o nome correto.
     */
    async deleteInstance(identifier) {
        if (!this.isConfigured) {
            return { success: false, error: 'Evolution API n√£o configurada. Verifique a API Key.' };
        }
        try {
            console.log(`üóëÔ∏è Iniciando exclus√£o para o identificador: ${identifier}`);

            // 1. Atualizar a lista de inst√¢ncias para garantir que o cache local est√° sincronizado
            await this.listInstances();

            let instanceName = identifier; // Por padr√£o, assume que o identificador √© o nome
            let foundById = false;

            // 2. Tentar encontrar o NOME da inst√¢ncia usando o IDENTIFICADOR como ID
            // Isso corrige o problema de IDs dessincronizados.
            for (const [name, status] of this.instanceStatus.entries()) {
                if (status.instanceId === identifier) {
                    instanceName = name;
                    foundById = true;
                    console.log(`‚úÖ Identificador ${identifier} corresponde ao nome de inst√¢ncia: ${instanceName}`);
                    break;
                }
            }

            if (!foundById) {
                console.log(`‚ö†Ô∏è  N√£o foi poss√≠vel encontrar uma inst√¢ncia com o ID ${identifier}. O sistema tentar√° excluir usando o valor como nome (comportamento legado).`);
            }

            console.log(`üóëÔ∏è  Tentando deletar inst√¢ncia na Evolution API com o nome: ${instanceName}`);
            
            // Parar polling se existir para o nome da inst√¢ncia encontrado
            this.stopQRCodePolling(instanceName);
            
            // 3. Chamar a API com o nome da inst√¢ncia resolvido
            const response = await axios.delete(
                `${this.baseURL}/instance/delete/${instanceName}`,
                {
                    headers: this.defaultHeaders,
                    timeout: 15000,
                    validateStatus: () => true // Aceitar qualquer status para tratar o 404 manualmente
                }
            );

            // 4. Tratar a resposta
            if (response.status < 400) {
                 console.log(`‚úÖ Inst√¢ncia ${instanceName} deletada com sucesso da Evolution API.`);
            } else if (response.status === 404) {
                console.log(`‚ö†Ô∏è Inst√¢ncia ${instanceName} n√£o encontrada na Evolution API (404), considerando como sucesso.`);
            } else {
                // Lan√ßar erro para outras falhas (500, etc)
                throw new Error(`Falha ao deletar na API: Status ${response.status} - ${JSON.stringify(response.data)}`);
            }
            
            // 5. Limpar caches locais com o nome da inst√¢ncia, independentemente do resultado
            this.qrCodeCache.delete(instanceName);
            this.instanceStatus.delete(instanceName);
            console.log(`üßπ Cache local para ${instanceName} foi limpo.`);
            
            return {
                success: true,
                message: `Inst√¢ncia ${instanceName} foi removida com sucesso.`
            };
            
        } catch (error) {
            console.error(`‚ùå Erro grave ao deletar inst√¢ncia com identificador ${identifier}:`, error.message);
            // Em caso de erro, tenta limpar o cache com o identificador original tamb√©m
            this.qrCodeCache.delete(identifier);
            this.instanceStatus.delete(identifier);
            
            return {
                success: false,
                error: error.message
            };
        }
    }

